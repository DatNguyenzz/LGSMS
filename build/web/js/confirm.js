function confirmFunction() {
    if (confirm("Bạn có chắc chắn với những thay đổi này?") == true) {
        alert("Lưu thành công!");
        location.reload();
    }
}
function confirmChangePass() {
    if (confirm("Bạn có chắc chắn với việc thay đổi mật khẩu này?") == true) {
        alert("Lưu thành công!");
        location.reload();
    }
}